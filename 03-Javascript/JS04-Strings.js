// JavaScript String Methods (Complete List)

// Initialization
let str = " Hello, JavaScript! ";

// Length
str.length                // ‚Üí number of characters

// Character access
str.charAt(0)             // ‚Üí character at index 0
str.charCodeAt(0)         // ‚Üí Unicode of character at index 0
str.codePointAt(0)        // ‚Üí Unicode code point (better for emojis)
str[0]                    // ‚Üí direct access (same as charAt)

// Searching
str.indexOf("Java")       // ‚Üí first index of "Java"
str.lastIndexOf("a")      // ‚Üí last index of "a"
str.includes("Java")      // ‚Üí true if "Java" is found
str.startsWith(" Hello")  // ‚Üí true
str.endsWith("!")         // ‚Üí true
str.search("Script")      // ‚Üí index using regex
str.match(/Java/g)        // ‚Üí matches regex, returns array
str.matchAll(/a/g)        // ‚Üí iterator for all matches (ES2020+)

// Modifying
str.toUpperCase()         // ‚Üí converts to upper case
str.toLowerCase()         // ‚Üí converts to lower case
str.replace("Java", "Type")    // ‚Üí replaces first match
str.replaceAll("a", "*")       // ‚Üí replaces all (ES2021+)
str.normalize()           // ‚Üí Unicode normalization

// Extracting
str.slice(1, 6)           // ‚Üí substring from index 1 to 5
str.substring(1, 6)       // ‚Üí similar to slice
str.substr(1, 5)          // ‚Üí deprecated, use slice

// Whitespace trimming
str.trim()                // ‚Üí removes both sides
str.trimStart()           // ‚Üí removes leading spaces
str.trimEnd()             // ‚Üí removes trailing spaces

// Padding
"5".padStart(4, "0")      // ‚Üí "0005"
"5".padEnd(4, "0")        // ‚Üí "5000"

// Repeating
"Ha".repeat(3)            // ‚Üí "HaHaHa"

// Splitting and joining
str.split(" ")            // ‚Üí splits into array
["Hello", "World"].join(" ")  // ‚Üí joins into string

// Comparison
"abc" === "abc"           // ‚Üí true (strict equality)
"abc".localeCompare("abd")   // ‚Üí -1, 0, or 1 for sorting

// Conversion
String(123)               // ‚Üí "123"
(123).toString()          // ‚Üí "123"


