/*
====================================
 WEB DESIGN RESEARCH & PLANNING NOTES
 Step-by-Step Sequence
====================================
*/

// 1. Topic Research – understand the niche or subject area.
// 2. Business Type – identify if it’s e-commerce, portfolio, blog, corporate, service, etc.
// 3. Goal of Website – define the main purpose (sell, inform, entertain, generate leads).
// 4. Target Audience – research demographics, needs, and device usage.
// 5. Competitors Analysis – study strengths, weaknesses, and design styles of similar sites.
// 6. Branding – decide logo, colors, typography, and tone of voice.
// 7. Color Theory – choose brand colors, ensure contrast & accessibility.
// 8. Inspiration Gathering – collect design ideas from Dribbble, Behance, Pinterest, etc.
// 9. Sitemap Creation – plan page hierarchy and navigation structure.
// 10. Content Plan – prepare text, headings, product descriptions, blog posts.
// 11. Wireframing – create low-fidelity layout sketches.
// 12. Prototype – create interactive mockups to visualize user flow before final design.
// 13. Layout Planning – arrange header, navigation, content areas, and footer.
// 14. Graphics & Imagery – select or design icons, illustrations, photos, videos.
// 15. Typography – choose readable and brand-appropriate fonts.
// 16. Navigation Flow – define menu structure, breadcrumbs, search options.
// 17. Responsive Design Planning – adapt layouts for desktop, tablet, and mobile.
// 18. Accessibility Considerations – color contrast, alt text, keyboard navigation.
// 19. Loading Speed Optimization – plan for optimized images and minimal scripts.
// 20. SEO in Design – use proper heading structure, meta tag planning.
// 21. Call-to-Action (CTA) Placement – plan buttons, forms, sign-ups.
// 22. Micro-interactions – hover effects, animations, user feedback.
// 23. User Experience (UX) Testing – check for intuitive, easy-to-use design.
// 24. Testing & Feedback Plan – usability testing, collect feedback, make revisions.
// 25. Final Review – ensure all elements align with goals and branding.
// 26. Launch Preparation – check hosting, domain, and technical setup.






/*
====================================
 COMMON WEBSITE COMPONENTS LIST
====================================
*/

// 1. Header – logo, navigation menu, search bar, language selector.
// 2. Navigation Bar (Navbar) – main site links, dropdown menus.
// 3. Hero Section – large banner with headline, subheading, and call-to-action.
// 4. Slider / Carousel – rotating images, testimonials, or featured products.
// 5. Features Section – key benefits or services.
// 6. About Section – company or personal story, mission, and values.
// 7. Services Section – list of offerings with descriptions and icons.
// 8. Portfolio / Work Showcase – gallery of past projects or products.
// 9. Testimonials – customer reviews and ratings.
// 10. Call-to-Action (CTA) Section – sign-up, contact, or buy now prompts.
// 11. Blog / News Section – list of recent articles or updates.
// 12. Contact Section – form, address, phone, map.
// 13. Pricing Section – packages, plans, and comparison tables.
// 14. FAQ Section – frequently asked questions with answers.
// 15. Newsletter Signup – email subscription form.
// 16. Sidebar – additional navigation, ads, or widgets.
// 17. Footer – copyright, links, social media icons.
// 18. Social Media Links – icons linking to profiles.
// 19. Search Bar – allows users to search the site.
// 20. Breadcrumbs – navigation trail showing page hierarchy.
// 21. Pagination – page number navigation for lists/blogs.
// 22. Filters & Sorting – for products, articles, or portfolios.
// 23. Modals / Popups – login forms, announcements, offers.
// 24. Login / Signup Forms – authentication UI.
// 25. Dashboard Widgets – stats, charts, quick actions (for web apps).
// 26. Progress Bars / Steps – multi-step forms, surveys, checkouts.
// 27. Gallery – image or video collections.
// 28. Video Section – embedded video player.
// 29. Map Integration – Google Maps or similar.
// 30. Chat Widget – live chat or chatbot.
// 31. Notification Bar – alerts, promotions, updates.
// 32. Language Selector – multi-language dropdown.
// 33. Loading Spinner / Skeleton Loader – loading state UI.
// 34. Back-to-Top Button – quick scroll to page top.
// 35. Cookie Consent Banner – GDPR compliance.
// 36. Error Pages – 404 / 500 custom pages.
// 37. Terms & Privacy Pages – legal information.
// 38. Sitemap Page – full list of site links.
// 39. Related Items Section – similar products or articles.
// 40. User Profile Section – account details, settings.






/*
====================================
 TOP 12 UI DESIGN PRINCIPLES
====================================
*/

// 1. Purpose – clearly define what the design is meant to achieve.
// 2. Colors – choose a balanced and consistent color palette.
// 3. Typography – select readable, brand-appropriate fonts.
// 4. Images – use high-quality, relevant visuals.
// 5. Navigation – ensure easy and intuitive user flow.
// 6. Grid-based Layout – align elements using a grid system.
// 7. Consistency – maintain uniform styles across the design.
// 8. F Design Pattern – structure content in an F-shaped reading pattern.
// 9. White Space – use empty space effectively for clarity.
// 10. Contrast – make important elements stand out.
// 11. Load-time Speed – optimize for fast loading.
// 12. Mobile Friendly – ensure responsive design for all devices.








