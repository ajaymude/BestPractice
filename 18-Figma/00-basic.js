/*
====================================
 WEB DESIGN RESEARCH & PLANNING NOTES
 Step-by-Step Sequence
====================================
*/

// 1. Topic Research – understand the niche or subject area.
// 2. Business Type – identify if it’s e-commerce, portfolio, blog, corporate, service, etc.
// 3. Goal of Website – define the main purpose (sell, inform, entertain, generate leads).
// 4. Target Audience – research demographics, needs, and device usage.
// 5. Competitors Analysis – study strengths, weaknesses, and design styles of similar sites.
// 6. Branding – decide logo, colors, typography, and tone of voice.
// 7. Color Theory – choose brand colors, ensure contrast & accessibility.
// 8. Inspiration Gathering – collect design ideas from Dribbble, Behance, Pinterest, etc.
// 9. Sitemap Creation – plan page hierarchy and navigation structure.
// 10. Content Plan – prepare text, headings, product descriptions, blog posts.
// 11. Wireframing – create low-fidelity layout sketches.
// 12. Layout Planning – arrange header, navigation, content areas, and footer.
// 13. Graphics & Imagery – select or design icons, illustrations, photos, videos.
// 14. Typography – choose readable and brand-appropriate fonts.
// 15. Navigation Flow – define menu structure, breadcrumbs, search options.
// 16. Responsive Design Planning – adapt layouts for desktop, tablet, and mobile.
// 17. Accessibility Considerations – color contrast, alt text, keyboard navigation.
// 18. Loading Speed Optimization – plan for optimized images and minimal scripts.
// 19. SEO in Design – use proper heading structure, meta tag planning.
// 20. Call-to-Action (CTA) Placement – plan buttons, forms, sign-ups.
// 21. Micro-interactions – hover effects, animations, user feedback.
// 22. User Experience (UX) Testing – check for intuitive, easy-to-use design.
// 23. Testing & Feedback Plan – usability testing, collect feedback, make revisions.
// 24. Final Review – ensure all elements align with goals and branding.
// 25. Launch Preparation – check hosting, domain, and technical setup. 

