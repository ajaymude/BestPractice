// Topic 119: Animating Route Transitions ‚Äì Detailed Notes and Code Examples
//
// Animating route transitions enhances UX by providing visual context when
// navigating between pages. In React Router v6 and frameworks like Next.js,
// use libraries like Framer Motion to animate components on mount and unmount.
//
// Key Concepts:
// 1. AnimatePresence:
//    ‚Ä¢ Wrap <Routes> or page components to enable exit animations.
//    ‚Ä¢ Needed for unmount animations as components leave the tree.
//
// 2. motion Components:
//    ‚Ä¢ Use <motion.div> or <motion.main> for page wrappers.
//    ‚Ä¢ Define initial, animate, and exit variants for transitions.
//
// 3. LayoutId for Shared Element Transitions:
//    ‚Ä¢ Assign a shared layoutId to elements to animate between positions across routes.
//
// 4. React Router Integration:
//    ‚Ä¢ Use useLocation to key <AnimatePresence> and <Routes> on pathname.
//
// Code Example: Basic Route Transitions (React Router v6)
import React from 'react';
import { Routes, Route, useLocation } from 'react-router-dom';
import { AnimatePresence, motion } from 'framer-motion';
import Home from './Home';
import About from './About';

export function AnimatedRoutes() {
  const location = useLocation();
  const variants = {
    initial: { opacity: 0, x: 100 },
    animate: { opacity: 1, x: 0 },
    exit: { opacity: 0, x: -100 }
  };

  return (
    <AnimatePresence exitBeforeEnter>
      <Routes location={location} key={location.pathname}>
        <Route path="/" element={
          <motion.div
            variants={variants}
            initial="initial"
            animate="animate"
            exit="exit"
            transition={{ duration: 0.3 }}
          >
            <Home />
          </motion.div>
        } />
        <Route path="/about" element={
          <motion.div
            variants={variants}
            initial="initial"
            animate="animate"
            exit="exit"
            transition={{ duration: 0.3 }}
          >
            <About />
          </motion.div>
        } />
      </Routes>
    </AnimatePresence>
  );
}

