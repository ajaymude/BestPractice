// Topic 110: Dark/Light Theme Toggling ‚Äì Detailed Notes and Code Examples
//
// Implementing dark/light mode allows users to switch between color schemes
// for better accessibility and user preference. Common approaches include
// CSS variables, context for global state, and persisting user choice.
//
// Key Concepts:
// 1. CSS Variables:
//    ‚Ä¢ Define theme colors in :root and [data-theme="dark"] selectors.
//    ‚Ä¢ Example:
//      :root { --bg: #fff; --text: #000; }
//      [data-theme="dark"] { --bg: #000; --text: #fff; }
//
// 2. Theme Context:
//    ‚Ä¢ Create React Context to provide current theme and toggle function.
//    ‚Ä¢ Consume context in components to apply or switch themes.
//
// 3. Persisting Preference:
//    ‚Ä¢ Store user choice in localStorage or system preference via matchMedia.
//    ‚Ä¢ Read stored/theme-media on load.
//
// 4. Toggles and Icons:
//    ‚Ä¢ Provide a toggle button that updates context and CSS attribute.
//    ‚Ä¢ Optionally use sun/moon icons for visual cue.
//
// Code Example: ThemeProvider with CSS Variables
import React, { createContext, useState, useEffect, useContext } from 'react';

const ThemeContext = createContext({ theme: 'light', toggle: () => {} });

export function ThemeProvider({ children }) {
  const [theme, setTheme] = useState('light');

  // On mount, read stored preference or system setting
  useEffect(() => {
    const stored = localStorage.getItem('theme');
    if (stored) setTheme(stored);
    else if (window.matchMedia('(prefers-color-scheme: dark)').matches) setTheme('dark');
  }, []);

  // Apply theme attribute and store
  useEffect(() => {
    document.documentElement.setAttribute('data-theme', theme);
    localStorage.setItem('theme', theme);
  }, [theme]);

  const toggle = () => setTheme(prev => (prev === 'light' ? 'dark' : 'light'));

  return (
    <ThemeContext.Provider value={{ theme, toggle }}>
      {children}
    </ThemeContext.Provider>
  );
}

export function useTheme() {
  return useContext(ThemeContext);
}

