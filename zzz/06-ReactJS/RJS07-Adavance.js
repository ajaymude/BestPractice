// Topic 117: Secure Token Handling (HttpOnly Cookies vs localStorage) ‚Äì Detailed Notes and Code Examples
//
// Secure token handling is essential to protect against XSS and CSRF attacks.
// Two primary storage approaches for JWTs or session tokens are HttpOnly cookies
// and browser storage (localStorage/sessionStorage). Each has trade-offs.
//
// 1. HttpOnly Cookies:
//    ‚Ä¢ Cookies set by the server with the HttpOnly flag cannot be accessed via JavaScript.
//    ‚Ä¢ Protects against XSS since scripts cannot read the token.
//    ‚Ä¢ Use SameSite and Secure flags to mitigate CSRF and ensure HTTPS-only transmission.
//    ‚Ä¢ Tokens auto-sent with every request to the cookie‚Äôs domain.
//
// Example: Server Sets HttpOnly Cookie (Express.js)
// import express from 'express';
// app.post('/login', (req, res) => {
//   const token = generateJwt(req.user);
//   res.cookie('token', token, {
//     httpOnly: true,
//     secure: true,
//     sameSite: 'lax',
//     maxAge: 24 * 60 * 60 * 1000, // 1 day
//   });
//   res.sendStatus(200);
// });
//
// Client Request (fetch includes credentials)
// fetch('/protected', { method: 'GET', credentials: 'include' });
//
// 2. localStorage/sessionStorage:
//    ‚Ä¢ JavaScript-accessible storage; vulnerable to XSS attacks.
//    ‚Ä¢ Easy to implement: setItem/getItem.
//    ‚Ä¢ Better control over expiration and manual cleanup.
//    ‚Ä¢ Requires manual inclusion of token in Authorization header.
//
// Example: Storing and Using Token in localStorage
// // On login success:
// localStorage.setItem('token', token);
// // API request:
// const token = localStorage.getItem('token');
// fetch('/api/data', {
//   headers: { 'Authorization': `Bearer ${token}` }
// });
//
// 3. Best Practices:
//    ‚Ä¢ Prefer HttpOnly cookies for enhanced security; pair with CSRF tokens or SameSite.
//    ‚Ä¢ Sanitize and validate all inputs to prevent XSS.
//    ‚Ä¢ Implement CSRF protection (double-submit cookie, hidden header token).
//    ‚Ä¢ Use short-lived tokens and refresh flows to limit exposure.
//    ‚Ä¢ Always use HTTPS to protect token in transit.
//
// 4. Token Refresh:
//    ‚Ä¢ Store refresh token in HttpOnly cookie; access token in-memory.
//    ‚Ä¢ On access token expiration, call /refresh endpoint (credentials: 'include') to renew.
//
// React Hook Example: useAuth for HttpOnly Cookie Flow
import { useState, useEffect, useCallback } from 'react';

export function useAuth() {
  const [user, setUser] = useState(null);
  const [loading, setLoading] = useState(true);

  const fetchUser = useCallback(async () => {
    try {
      const res = await fetch('/me', { credentials: 'include' });
      if (res.ok) setUser(await res.json());
      else setUser(null);
    } catch {
      setUser(null);
    } finally {
      setLoading(false);
    }
  }, []);

  useEffect(() => { fetchUser(); }, [fetchUser]);

  const login = async (credentials) => {
    const res = await fetch('/login', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify(credentials),
      credentials: 'include',
    });
    if (res.ok) await fetchUser();
    return res.ok;
  };

  const logout = async () => {
    await fetch('/logout', { method: 'POST', credentials: 'include' });
    setUser(null);
  };

  return { user, loading, login, logout };
}

// Notes:
// ‚Ä¢ HttpOnly cookies minimize XSS risk; localStorage offers ease but less secure.
// ‚Ä¢ Use credentials: 'include' in fetch to send cookies.
// ‚Ä¢ Maintain authentication state in-memory, not in localStorage, when using cookies.
// ‚Ä¢ Always clear cookies and storage on logout to prevent token reuse.

