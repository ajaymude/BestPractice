// O(1) means the time complexity of the algorithm is constant
// O(n^2) means the time complexity of the algorithm is quadratic
// O(log n) means the time complexity of the algorithm is logarithmic
// O(n log n) means the time complexity of the algorithm is linearithmic
// O(n^3) means the time complexity of the algorithm is cubic
// O(2^n) means the time complexity of the algorithm is exponential
// O(n!) means the time complexity of the algorithm is factorial

