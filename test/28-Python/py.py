# 📘 What is a Set?
# A set is an unordered collection of unique elements. It does not allow
# duplicate items and does not guarantee the order of elements. Sets are commonly used 
# for operations like membership tests, removing duplicates, 
# and performing mathematical operations like union, intersection, etc.


my_set = {1, 2, 3, 4, 5}
another_set = set([10, 20, 30, 30])  # Duplicates are automatically removed
empty_set = set()  # Empty set


my_set = {1, 2, 3, 4, 5}
another_set = set([10, 20, 30, 30])  # Duplicates are automatically removed
empty_set = set()  # Empty set


# Creating sets
my_set = {1, 2, 3, 4, 5}
another_set = set([10, 20, 30, 30])  # Duplicates are removed automatically
empty_set = set()

print("Initial Sets:")
print(my_set)        # {1, 2, 3, 4, 5}
print(another_set)   # {10, 20, 30}
print(empty_set)     # set()

# Adding an element
my_set.add(6)
print("\nAfter adding an element:")
print(my_set)        # {1, 2, 3, 4, 5, 6}

# Removing an element (raises an error if the element is not found)
my_set.remove(3)
print("\nAfter removing element 3:")
print(my_set)        # {1, 2, 4, 5, 6}

# Discarding an element (does nothing if the element is not found)
my_set.discard(7)    # Element 7 doesn't exist, no error
print("\nAfter discarding element 7:")
print(my_set)        # {1, 2, 4, 5, 6}

# Popping an arbitrary element
popped_item = my_set.pop()
print("\nAfter popping an element (arbitrary):")
print("Popped item:", popped_item)
print(my_set)        # Set with one element removed (arbitrary)

# Clearing the set
my_set.clear()
print("\nAfter clearing the set:")
print(my_set)        # set()

# Copying the set
my_set = {1, 2, 3, 4, 5}
new_set = my_set.copy()
print("\nOriginal and copied set:")
print(my_set)        # {1, 2, 3, 4, 5}
print(new_set)       # {1, 2, 3, 4, 5}

# Union (combines two sets)
set_a = {1, 2, 3}
set_b = {3, 4, 5}
union_set = set_a | set_b  # or set_a.union(set_b)
print("\nUnion of sets A and B:")
print(union_set)    # {1, 2, 3, 4, 5}

# Intersection (common elements between two sets)
intersection_set = set_a & set_b  # or set_a.intersection(set_b)
print("\nIntersection of sets A and B:")
print(intersection_set)  # {3}

# Difference (elements in set_a but not in set_b)
difference_set = set_a - set_b  # or set_a.difference(set_b)
print("\nDifference of sets A and B (A - B):")
print(difference_set)  # {1, 2}

# Symmetric Difference (elements that are in either set but not in both)
symmetric_difference_set = set_a ^ set_b  # or set_a.symmetric_difference(set_b)
print("\nSymmetric Difference of sets A and B:")
print(symmetric_difference_set)  # {1, 2, 4, 5}

# Set Length
print("\nLength of set A:")
print(len(set_a))   # 3

# Checking membership
print("\nChecking membership in set A:")
print(1 in set_a)   # True
print(6 in set_a)   # False




