# Input/Output (I/O) with Files in Python
# In Python, working with files is essential for many tasks, such 
# as reading and writing data to a file. Python provides built-in
# functions to handle file I/O operations.



# Writing to a file (overwriting the content)
with open("example.txt", "w") as file:
    file.write("This is a new file.\n")
    file.write("It contains multiple lines.\n")

# Reading from the file
with open("example.txt", "r") as file:
    content = file.read()
    print("Content of file:")
    print(content)

# Appending to the file
with open("example.txt", "a") as file:
    file.write("This line is appended.\n")

# Reading the file again to show appended content
with open("example.txt", "r") as file:
    content = file.read()
    print("\nUpdated content of file:")
    print(content)

# Writing a list of strings to the file using writelines()
lines = ["Line 1\n", "Line 2\n", "Line 3\n"]
with open("example.txt", "w") as file:
    file.writelines(lines)

# Reading lines from the file (as a list of strings)
with open("example.txt", "r") as file:
    lines = file.readlines()
    print("\nLines read from the file:")
    print(lines)

# Checking the file mode when opening
file = open("example.txt", "r")
print("\nFile is opened in mode:", file.mode)
file.close()

# Writing to a file and closing explicitly
file = open("example.txt", "w")
file.write("This is a new text written directly with open().\n")
file.close()

# Reading from a file using readline() (one line at a time)
with open("example.txt", "r") as file:
    line1 = file.readline()
    print("\nFirst line using readline():", line1)

# Reading the file line by line using a loop
with open("example.txt", "r") as file:
    print("\nReading file line by line:")
    for line in file:
        print(line.strip())  # .strip() removes the trailing newline

# Using try-except to handle file errors (e.g., file not found)
try:
    with open("non_existent_file.txt", "r") as file:
        content = file.read()
except FileNotFoundError:
    print("\nError: The file does not exist!")





